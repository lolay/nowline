#!/usr/bin/env node

// Rebuilds the complete `embed.nowline.io` version history into a Firebase
// Hosting `public/` tree, using npm as the single canonical source of bytes
// for every version. Run at deploy time (release.yml `embed-prod` job, wired
// in by a later step).
//
// Why npm is the source of bytes
// ------------------------------
// Firebase Hosting deploys are atomic full-site replacements, so every
// release must lay down ALL hosted versions, not just the current one.
// Rather than depend on fragile origin state (reading back whatever is
// already deployed), the deploy reconstructs the whole tree from npm each
// release — which makes it idempotent and self-healing. npm is the one
// canonical source of bytes for every version:
//   - Current version: bytes come from the local `npm-tarballs` build
//     artifact (`nowline-embed-{version}.tgz`) — the exact tarball that gets
//     published. Taken pre-publish, so there is no npm-propagation race, and
//     the CDN's current file is literally the same bytes npm will serve.
//   - Prior versions: downloaded from the npm registry and extracted as-is.
//     History is byte-exact by construction — we fetch published bytes and
//     never rebuild old versions, so toolchain drift over time is irrelevant.
//
// Byte-identity guard (the reason this script exists)
// ---------------------------------------------------
// The current version is the only place two independent builds of the same
// tag must agree: the CDN file (this script, from the npm tarball) vs the
// `embed-cdn-prod` artifact (built separately by the pack-embed cell). With
// the deterministic banner (NOWLINE_EMBED_BUILT_AT / commit date, applied in
// bundle.mjs) the two builds are byte-identical, so we ASSERT equality and
// fail loudly on mismatch — catching any determinism regression before it
// ships. This is what makes immutability real: when a version later
// transitions from "current" to "reconstructed-from-registry", its bytes
// don't change.
//
// Layout produced (prod hosting target)
// -------------------------------------
//   public/{X.Y.Z}/nowline.min.js (+ .map)   immutable per-patch pin
//   public/{X.Y}/nowline.min.js   (+ .map)    alias -> highest patch in minor
//   public/latest/nowline.min.js  (+ .map)    alias -> highest overall
// Aliases are BYTE COPIES, not redirects/rewrites, so: each path keeps its
// own Cache-Control (immutable for X.Y.Z, short for X.Y/latest — Firebase
// matches headers on the request path), the mutable channels can be cleanly
// overwritten on every full-site deploy (a 301 would get stuck), and each
// dir's relative `./nowline.min.js` demo resolves. The per-version
// `index.html` demo pages are generated by a separate step; this script only
// lays down the bundle + map.
//
// Idempotency / self-healing
// --------------------------
// Re-running converges the managed set to the correct bytes: missing
// version/alias dirs are recreated, wrong bytes overwritten, and a stale
// alias map (from a legacy version that lost its map) is dropped. It does
// NOT prune unrelated paths — immutable pins are never removed and the demo
// `index.html` files are left intact.
//
// CLI
// ---
//   node build-cdn-history.mjs \
//     --public <dir>          (required) Firebase `public/` dir to populate
//     --version <X.Y.Z>       (required) current release version (no `v`)
//     --npm-tarballs <dir>    (required) `npm-tarballs` artifact dir holding
//                             nowline-embed-{version}.tgz (current bytes)
//     --embed-cdn-prod <dir>  (required) `embed-cdn-prod` artifact dir holding
//                             {version}/nowline.min.js (byte-identity guard)
//     --package-name <name>   (optional) defaults to @nowline/embed
//     --prior-tarballs <dir>  (optional) offline/fixture mode: read prior
//                             versions from local <prefix>-*.tgz here instead
//                             of querying the npm registry
//
// Leaf operations shell out to `tar` (extraction) and `npm` (`npm view`)
// where that's simplest; registry downloads use the built-in `fetch`. No new
// dependencies — semver ordering is a tiny numeric comparator because the
// release process only ever cuts plain MAJOR.MINOR.PATCH versions.

import { execFileSync } from 'node:child_process';
import { createHash } from 'node:crypto';
import {
    copyFileSync,
    existsSync,
    mkdirSync,
    mkdtempSync,
    readdirSync,
    readFileSync,
    rmSync,
    writeFileSync,
} from 'node:fs';
import { tmpdir } from 'node:os';
import { basename, join, resolve } from 'node:path';

const DEFAULT_PACKAGE = '@nowline/embed';
const BUNDLE = 'nowline.min.js';
const MAP = 'nowline.min.js.map';
const DOWNLOAD_ATTEMPTS = 3;

// ---- argument parsing -------------------------------------------------------

function parseArgs(argv) {
    const args = {};
    for (let i = 0; i < argv.length; i++) {
        const tok = argv[i];
        if (!tok.startsWith('--')) {
            throw new Error(`unexpected argument "${tok}" (expected --key value)`);
        }
        const eq = tok.indexOf('=');
        if (eq !== -1) {
            args[tok.slice(2, eq)] = tok.slice(eq + 1);
            continue;
        }
        const val = argv[i + 1];
        if (val === undefined || val.startsWith('--')) {
            throw new Error(`flag "${tok}" expects a value`);
        }
        args[tok.slice(2)] = val;
        i++;
    }
    return args;
}

function requireArg(args, name) {
    const v = args[name];
    if (v === undefined || v === '') {
        throw new Error(`missing required flag --${name}`);
    }
    return v;
}

// ---- semver ordering --------------------------------------------------------

const VERSION_RE = /^\d+\.\d+\.\d+$/;

function parseVersion(v) {
    if (!VERSION_RE.test(v)) {
        throw new Error(
            `unsupported version "${v}": expected plain MAJOR.MINOR.PATCH. The ` +
                'release process only cuts plain semver (no prerelease/build ' +
                'suffixes), so a value here means an unexpected tag reached the ' +
                'deploy and should be investigated rather than silently ordered.',
        );
    }
    return v.split('.').map(Number);
}

// -1 if a < b, 0 if equal, 1 if a > b — usable directly as Array#sort comparator.
function compareVersions(a, b) {
    const pa = parseVersion(a);
    const pb = parseVersion(b);
    for (let i = 0; i < 3; i++) {
        if (pa[i] !== pb[i]) return pa[i] < pb[i] ? -1 : 1;
    }
    return 0;
}

// ---- byte-identity guard ----------------------------------------------------

function sha256(buf) {
    return createHash('sha256').update(buf).digest('hex');
}

function firstDiffOffset(a, b) {
    const n = Math.min(a.length, b.length);
    for (let i = 0; i < n; i++) {
        if (a[i] !== b[i]) return i;
    }
    return a.length === b.length ? -1 : n;
}

// Fail-loud equality assertion. On mismatch the message carries both sha256s,
// both sizes, and the first differing offset so a determinism regression is
// unmistakable in the deploy log.
function assertBytesEqual(label, expected, actual) {
    if (expected.equals(actual)) return;
    throw new Error(
        `byte-identity guard FAILED for ${label}:\n` +
            `  npm-tarball   : ${expected.length} bytes  sha256=${sha256(expected)}\n` +
            `  embed-cdn-prod: ${actual.length} bytes  sha256=${sha256(actual)}\n` +
            `  first differing byte at offset ${firstDiffOffset(expected, actual)}\n` +
            'The current-version bundle from the npm tarball does not match the\n' +
            'embed-cdn-prod artifact. With the deterministic banner these MUST be\n' +
            'byte-identical; a mismatch means a build-determinism regression.',
    );
}

// ---- tarball extraction -----------------------------------------------------

// pnpm/npm pack strips the `@scope/` and joins with `-`, so `@nowline/embed`
// packs to `nowline-embed-{version}.tgz`.
function tarballPrefix(pkg) {
    return pkg.replace(/^@/, '').replace(/\//g, '-');
}

function escapeRegExp(s) {
    return s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
}

// Extracts a package tarball into a throwaway temp dir and reads the embed
// bundle out of `package/dist/`. The `.map` is optional: legacy tarballs
// predate it, so a missing map is a graceful skip (returns map: null), not an
// error.
function readBundleFromTarball(tgzPath) {
    if (!existsSync(tgzPath)) {
        throw new Error(`tarball not found: ${tgzPath}`);
    }
    const tmp = mkdtempSync(join(tmpdir(), 'cdn-history-'));
    try {
        execFileSync('tar', ['-xzf', tgzPath, '-C', tmp], {
            stdio: ['ignore', 'ignore', 'inherit'],
        });
        const jsPath = join(tmp, 'package', 'dist', BUNDLE);
        if (!existsSync(jsPath)) {
            throw new Error(`tarball ${basename(tgzPath)} has no package/dist/${BUNDLE}`);
        }
        const mapPath = join(tmp, 'package', 'dist', MAP);
        return {
            js: readFileSync(jsPath),
            map: existsSync(mapPath) ? readFileSync(mapPath) : null,
        };
    } finally {
        rmSync(tmp, { recursive: true, force: true });
    }
}

// ---- npm registry -----------------------------------------------------------

function npmViewVersions(pkg) {
    const out = execFileSync('npm', ['view', pkg, 'versions', '--json'], { encoding: 'utf-8' });
    const parsed = JSON.parse(out);
    // npm returns a bare string when a package has a single published version.
    return Array.isArray(parsed) ? parsed : [parsed];
}

function npmViewTarballUrl(pkg, version) {
    const out = execFileSync('npm', ['view', `${pkg}@${version}`, 'dist.tarball', '--json'], {
        encoding: 'utf-8',
    });
    return JSON.parse(out);
}

async function downloadToFile(url, destPath) {
    let lastErr;
    for (let attempt = 1; attempt <= DOWNLOAD_ATTEMPTS; attempt++) {
        try {
            const res = await fetch(url);
            if (!res.ok) {
                throw new Error(`HTTP ${res.status} ${res.statusText}`);
            }
            writeFileSync(destPath, Buffer.from(await res.arrayBuffer()));
            return;
        } catch (err) {
            lastErr = err;
            if (attempt < DOWNLOAD_ATTEMPTS) {
                await new Promise((r) => setTimeout(r, attempt * 500));
            }
        }
    }
    throw new Error(
        `failed to download ${url} after ${DOWNLOAD_ATTEMPTS} attempts: ${lastErr.message}`,
    );
}

// Resolves a prior version's bundle either from a local fixture tarball
// (offline mode) or by resolving + downloading its registry tarball.
async function readPriorBundle(pkg, version, priorTarballsDir) {
    const prefix = tarballPrefix(pkg);
    if (priorTarballsDir) {
        return readBundleFromTarball(join(priorTarballsDir, `${prefix}-${version}.tgz`));
    }
    const url = npmViewTarballUrl(pkg, version);
    const tmp = mkdtempSync(join(tmpdir(), 'cdn-history-dl-'));
    try {
        const tgz = join(tmp, `${prefix}-${version}.tgz`);
        await downloadToFile(url, tgz);
        return readBundleFromTarball(tgz);
    } finally {
        rmSync(tmp, { recursive: true, force: true });
    }
}

function discoverPriorTarballVersions(dir, prefix) {
    const re = new RegExp(`^${escapeRegExp(prefix)}-(\\d+\\.\\d+\\.\\d+)\\.tgz$`);
    return readdirSync(dir)
        .map((f) => f.match(re))
        .filter((m) => m !== null)
        .map((m) => m[1]);
}

// ---- public/ tree -----------------------------------------------------------

function writeBundle(publicDir, version, bundle) {
    const dir = join(publicDir, version);
    mkdirSync(dir, { recursive: true });
    writeFileSync(join(dir, BUNDLE), bundle.js);
    if (bundle.map) {
        writeFileSync(join(dir, MAP), bundle.map);
    }
}

// alias -> source version, computed over the full version set: each minor
// maps to its highest patch, plus `latest` -> highest overall.
function computeAliasTargets(versions) {
    const minorBest = new Map();
    let latest = null;
    for (const v of versions) {
        const [maj, min] = v.split('.');
        const minorKey = `${maj}.${min}`;
        const curMinor = minorBest.get(minorKey);
        if (!curMinor || compareVersions(v, curMinor) > 0) minorBest.set(minorKey, v);
        if (!latest || compareVersions(v, latest) > 0) latest = v;
    }
    const targets = new Map(minorBest);
    if (latest) targets.set('latest', latest);
    return targets;
}

// Aliases are byte copies of the chosen patch dir, sourced from the patch dir
// this script already wrote so the alias is guaranteed identical to its pin.
function copyAlias(publicDir, alias, sourceVersion) {
    const srcDir = join(publicDir, sourceVersion);
    const dstDir = join(publicDir, alias);
    mkdirSync(dstDir, { recursive: true });
    copyFileSync(join(srcDir, BUNDLE), join(dstDir, BUNDLE));
    const srcMap = join(srcDir, MAP);
    const dstMap = join(dstDir, MAP);
    if (existsSync(srcMap)) {
        copyFileSync(srcMap, dstMap);
    } else if (existsSync(dstMap)) {
        // Self-heal: the chosen patch has no map, so drop any stale alias map
        // left by a previous run.
        rmSync(dstMap);
    }
}

// ---- main -------------------------------------------------------------------

async function main() {
    const args = parseArgs(process.argv.slice(2));
    const version = requireArg(args, 'version');
    const publicDir = resolve(requireArg(args, 'public'));
    const npmTarballsDir = resolve(requireArg(args, 'npm-tarballs'));
    const embedCdnProdDir = resolve(requireArg(args, 'embed-cdn-prod'));
    const pkg = args['package-name'] ?? DEFAULT_PACKAGE;
    const priorTarballsDir = args['prior-tarballs'] ? resolve(args['prior-tarballs']) : null;

    parseVersion(version); // fail loud before doing any work
    const prefix = tarballPrefix(pkg);
    mkdirSync(publicDir, { recursive: true });

    // 1. Current version — bytes from the npm-tarballs artifact (single source
    //    of bytes), guarded byte-for-byte against the embed-cdn-prod artifact.
    const currentTgz = join(npmTarballsDir, `${prefix}-${version}.tgz`);
    if (!existsSync(currentTgz)) {
        throw new Error(
            `current tarball not found: ${currentTgz}\n` +
                `--npm-tarballs dir contains: ${readdirSync(npmTarballsDir).join(', ')}`,
        );
    }
    const current = readBundleFromTarball(currentTgz);

    const cdnVersionDir = join(embedCdnProdDir, version);
    const cdnJsPath = join(cdnVersionDir, BUNDLE);
    if (!existsSync(cdnJsPath)) {
        throw new Error(`embed-cdn-prod is missing ${version}/${BUNDLE} (looked at ${cdnJsPath})`);
    }
    assertBytesEqual(`${version}/${BUNDLE}`, current.js, readFileSync(cdnJsPath));

    const cdnMapPath = join(cdnVersionDir, MAP);
    const cdnHasMap = existsSync(cdnMapPath);
    if (current.map && cdnHasMap) {
        assertBytesEqual(`${version}/${MAP}`, current.map, readFileSync(cdnMapPath));
    } else if (Boolean(current.map) !== cdnHasMap) {
        console.warn(
            `WARN: source-map presence differs for ${version} ` +
                `(npm tarball ${current.map ? 'has' : 'lacks'} a map, embed-cdn-prod ` +
                `${cdnHasMap ? 'has' : 'lacks'} one). Continuing, but two builds of ` +
                'the same tag should agree.',
        );
    }

    writeBundle(publicDir, version, current);
    console.log(`current ${version}: bytes from npm tarball; byte-identity guard passed`);

    // 2. Prior versions — fetched from the npm registry (or local fixtures).
    //    Byte-exact by construction (we copy published bytes, never rebuild).
    const priorVersions = (
        priorTarballsDir
            ? discoverPriorTarballVersions(priorTarballsDir, prefix)
            : npmViewVersions(pkg)
    )
        .filter((v) => v !== version)
        .sort(compareVersions);
    for (const v of priorVersions) {
        const bundle = await readPriorBundle(pkg, v, priorTarballsDir);
        writeBundle(publicDir, v, bundle);
        console.log(`prior ${v}: ${bundle.map ? 'js + map' : 'js (legacy, no map)'}`);
    }

    // 3. Recompute X.Y and latest aliases over the full set, as byte copies.
    const allVersions = [version, ...priorVersions];
    const aliases = computeAliasTargets(allVersions);
    for (const [alias, sourceVersion] of aliases) {
        copyAlias(publicDir, alias, sourceVersion);
        console.log(`alias ${alias} -> ${sourceVersion}`);
    }

    console.log(
        `done: ${allVersions.length} version dir(s) + ${aliases.size} alias(es) under ${publicDir}`,
    );
}

main().catch((err) => {
    console.error(err.message ?? err);
    process.exit(1);
});
