#!/usr/bin/env node
// Codegen: read assets/fonts/*.ttf and emit src/generated/bundled-fonts.ts
// with the bytes as base64 string literals.
//
// Why: bun --compile auto-embeds explicit asset imports and .node native
// modules, but it does NOT virtualize fs.readFile of import.meta.url paths.
// Embedding the bytes as string literals is the simplest cross-runtime fix
// (works under both Node and bun-compiled). Mirrors the
// packages/cli/scripts/bundle-templates.mjs pattern.
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import * as path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const packageRoot = path.resolve(__dirname, '..');
const assetsDir = path.join(packageRoot, 'assets', 'fonts');
const outDir = path.join(packageRoot, 'src', 'generated');

const FONTS = [
    { constant: 'SANS_BASE64', file: 'DejaVuSans.ttf' },
    { constant: 'MONO_BASE64', file: 'DejaVuSansMono.ttf' },
];

mkdirSync(outDir, { recursive: true });

const lines = [
    '// Auto-generated by scripts/bundle-fonts.mjs. Do not edit by hand.',
    '// Run `pnpm run bundle-fonts` to regenerate.',
    '//',
    '// Bytes are base64-encoded so the loader works under both Node and a',
    '// bun --compile binary (which does not auto-embed runtime fs.readFile',
    '// targets).',
    '',
];

for (const { constant, file } of FONTS) {
    const abs = path.join(assetsDir, file);
    const bytes = readFileSync(abs);
    const b64 = bytes.toString('base64');
    lines.push(`export const ${constant} = ${JSON.stringify(b64)};`);
    lines.push('');
}

const outFile = path.join(outDir, 'bundled-fonts.ts');
writeFileSync(outFile, lines.join('\n'));
console.log(`wrote ${path.relative(packageRoot, outFile)}`);
