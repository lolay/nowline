#!/usr/bin/env node
// Regenerates `src/generated/showcase.ts` from `examples/showcase.nowline`.
// Runs as `prebuild` / `pretest` so `showcaseSource` is always in sync with
// the on-disk example.

import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import * as path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const packageRoot = path.resolve(__dirname, '..');
const repoRoot = path.resolve(packageRoot, '..', '..');
const sourceFile = path.join(repoRoot, 'examples', 'showcase.nowline');
const outDir = path.join(packageRoot, 'src', 'generated');
const outFile = path.join(outDir, 'showcase.ts');

mkdirSync(outDir, { recursive: true });

const contents = readFileSync(sourceFile, 'utf-8');

const generated = [
    '// AUTO-GENERATED by `packages/browser/scripts/bundle-showcase.mjs`.',
    '// Do not edit by hand; this file is regenerated from',
    '// `examples/showcase.nowline` on every build / test run.',
    '',
    `export const showcaseSource = ${JSON.stringify(contents)};`,
    '',
].join('\n');

writeFileSync(outFile, generated);
console.log(`wrote ${path.relative(packageRoot, outFile)}`);
